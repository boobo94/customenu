<template>
  <v-container class="pb-15">
    <v-row align="center" justify="center">
      <v-progress-circular
        v-if="isLoading"
        indeterminate
        color="primary"
      ></v-progress-circular>
    </v-row>

    <v-row v-if="!isLoading">
      <v-col cols="12" v-for="element in elements" :key="element.id">
        <ProductInList v-bind="element" />
      </v-col>
    </v-row>
  </v-container>
</template>

<script>
import ProductInList from '@/components/ProductInList.vue';
import axios from 'axios';

export default {
  name: 'Products',
  components: {
    ProductInList,
  },
  data() {
    return {
      isLoading: true,
      elements: [],
    };
  },
  async created() {
    const { restaurantUrl, categoryId } = this.$route.params;
    const { data } = await axios.get(
      `/web/v1/${restaurantUrl}/categories/${categoryId}/products`,
    );

    this.isLoading = false;
    this.elements = data;
  },
  methods: {},
};
</script>
